    @Override
    public void display(Customer element) {

        String[] columnNames = {"ID","Nombre","Fecha","Telefono","Correo","Edad"};
        DefaultTableModel table = new DefaultTableModel(columnNames, 0);
        JTable txttabla = new JTable(table);
        table.setRowCount(0);
        if (element!=null) {
            Object[] rowData={
                element.getId(),
                element.getName(),
                element.getBirthday(),
                element.getPhone(),
                element.getEmail(),
                element.getAge()
            };
            table.addRow(rowData);
        }else{
         JOptionPane.showMessageDialog(null, "No hay clientes registrados.");

        }
    }

