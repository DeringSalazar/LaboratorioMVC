    @Override
    public void insert() {
          if (controller != null) {
            controller.insert(
                Integer.parseInt(txtID.getText()),
                txtName.getText(),
                UtilDate.stringToLocalDate(txtDate.getText()),
                txtPhone.getText(),
                txtEmail.getText()
            );

            txtID.setText(""); 
            txtName.setText("");
            txtDate.setText("");
            txtPhone.setText("");
            txtEmail.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "El controlador no está inicializado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

